void pack_and_sendMessage(struct FIFO *fifo,int cpu_num,int addr, int data){
  struct Message* temp = (struct Message*)malloc(sizeof(struct Message));
  temp->addr = addr;
  temp->data = data;
  temp->next = NULL;
  temp->dest = cpu_num;
  if(fifo->back == NULL){
		fifo->front = fifo->back = temp;
		fifo->size+=1;
	}else{
		fifo->back->next = temp;
		fifo->back = fifo->back->next;
		fifo->size+=1;
	}
}

